  ret = {

      'links'     :  'https://mp.weixin.qq.com/s/1NvmV-dOeMrlDPVLirt7vQ',

      'huixian'   :  '',

      'method'    :   'get',

      'bugname'   :   'Grafana信息泄露 CVE-2022-26148',

      'level'     :   'high',

      'FOFA'      :   '',

      'author'    :   'ppxfx',

      'ifbug'     :   False,
  }
